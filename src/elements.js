function createCard(cardImg, elemNumber, elemSymbol, elemName) {
    var card = document.createElement('div');

    var header = document.createElement('div');
    var img = document.createElement('div');
    var footer = document.createElement('div');

    // Create a header with the element number.
    header.classList.add("elem-card-text-top");
    header.textContent = elemNumber;

    // Display the element symbol.
    card.classList.add("elem-card");
    img.classList.add("elem-img-div");
    img.classList.add(cardImg);
    img.textContent = elemSymbol;

    // Create a footer with the element name.
    footer.classList.add("elem-card-text-bottom");
    footer.textContent = elemName;

    card.appendChild(header);
    card.appendChild(img);
    card.appendChild(footer);

    return card;
}

function createPeriodicTable() {
    var container = document.createElement('div');
    container.classList.add("elem-container");
    document.body.appendChild(container);



    doRow(row1);
    doRow(row2);
    doRow(row3);
    doRow(row4);
    doRow(row5);
    doRow(row6);
    doRow(row7);

    doRow_LanthanideActinide(row_Lanthanide, "Lanthanide");
    doRow_LanthanideActinide(row_Actinide, "Actinide");
}

// Helper function to create each row of the Periodic table.
function doRow(rowNum) {
    var n = 18;     // Number of columns.

    var container = document.createElement('div');
    container.classList.add("elem-container");
    document.body.appendChild(container);

    for (var i = 0; i < n; i++) {
        var col = i % 3 + 1;

        var c1;
        if (!rowNum[i].hasOwnProperty('Symbol')) {
            c1 = createCard("img-div-col0", rowNum[i].Number, rowNum[i].Symbol, rowNum[i].Name);
            c1.style.visibility = 'hidden';
        }

        else if (rowNum[i].Symbol == '') {
            c1 = createCard("img-div-col0", rowNum[i].Number, rowNum[i].Symbol, rowNum[i].Name);
        }

        else {
            var divColour;
            switch (rowNum[i].Type) {
                case 'Metalloid': divColour = 'img-div-col1'; break;
                case 'Alkali Metal': divColour = 'img-div-col2'; break;
                case 'Alkaline Earth': divColour = 'img-div-col3'; break;
                case 'Transition Metal': divColour = 'img-div-col4'; break;
                case 'Basic Metal': divColour = 'img-div-col5'; break;
                case 'Non-metal': divColour = 'img-div-col6'; break;
                case 'Halogen': divColour = 'img-div-col7'; break;
                case 'Noble Gas': divColour = 'img-div-col8'; break;
                default: divColour = 'img-div-col9'; break;
            }
            c1 = createCard(divColour, rowNum[i].Number, rowNum[i].Symbol, rowNum[i].Name);
        }

        container.appendChild(c1);
    }
}

// Helper function to display the Lanthanide and Actinide series of elements.
function doRow_LanthanideActinide(rowNum) {
    var n = 15;     // Number of columns.

    var container = document.createElement('div');
    container.classList.add("elem-container");
    document.body.appendChild(container);

    var typeName = rowNum[0].Type;
    var divTypeName = document.createElement('div');
    divTypeName.textContent = typeName + " Series";
    divTypeName.classList.add("elem-container-La-Ac");
    divTypeName.classList.add("title-La-Ac");
    container.appendChild(divTypeName);

    for (var i = 0; i < n; i++) {
        var c1;
        var divColour;
        switch (rowNum[i].Type) {
            case 'Lanthanide': divColour = 'img-div-col9'; break;
            case 'Actinide': divColour = 'img-div-col10'; break;
            default: divColour = 'img-div-col9'; break;
        }
        c1 = createCard(divColour, rowNum[i].Number, rowNum[i].Symbol, rowNum[i].Name);

        container.appendChild(c1);
    }
}

function createLegendBox() {
    var legendBox = document.createElement('div');
    legendBox.id = "legend";
    legendBox.classList.add("legend-box");
    document.body.appendChild(legendBox);

    createLegendItem("Metalloid", "img-div-col1");
    createLegendItem("Alkali Metal", "img-div-col2");
    createLegendItem("Alkaline Earth", "img-div-col3");
    createLegendItem("Transition Metal", "img-div-col4");
    createLegendItem("Basic Metal", "img-div-col5");
    createLegendItem("Non-metal", "img-div-col6");
    createLegendItem("Halogen", "img-div-col7");
    createLegendItem("Noble Gas", "img-div-col8");
}

function createLegendItem(elemType, itemClass) {
    // Create the container for the colour and name.
    var legendItem = document.createElement('div');
    legendItem.classList.add("legend-item");

    // Legend colour
    var legendItemColour = document.createElement('div');
    legendItemColour.classList.add("legend-item-col");
    legendItemColour.classList.add(itemClass);

    // Legend name
    var legendItemName = document.createElement('div');
    legendItemName.textContent = elemType;

    // Add the colour and name to the container div.
    legendItem.appendChild(legendItemColour);
    legendItem.appendChild(legendItemName);

    // Add the new item to the Legend box.
    var legendBox = document.getElementById('legend');
    legendBox.appendChild(legendItem);
}